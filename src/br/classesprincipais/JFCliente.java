public boolean verificaNome(String nome) throws Exception {  
    boolean achou = false;  
    String sql = "select nome_cliente from cliente";  
    con.conecta();
    con.executaSql(sql);     
    while (!achou  && con.getRs().next()) {  
        if (nome.equalsIgnoreCase(con.getRs().getString("nome_cliente"))) {  
            achou = true;  
        }  
    }  
    con.getRs().close();  
    con.getPs().close();  
    return achou;  
}  
     
