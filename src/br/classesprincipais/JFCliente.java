    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
           /*caso nao tenha clicado no botao novo, 
            o valor da variavel alterar, estará em 0*/
           if(alterar==1)
           {

               if((verificaNome(jTnome.getText())!=true) || (jTnome.getText().equalsIgnoreCase(nomeselecionado)))
               {
                   con.conecta();
                   con.executaSql("update  cliente set nome_cliente='"+jTnome.getText()+"',"
                           + "telefone='"+jTtel.getText()+"', cpf='"+jTcpf.getText()+"', endereco='"+jTendereco.getText()+"',"
                           + "sexo='"+sexo+"',cidade_codCidade="+jTcidade.getText()+
                           " where codCliente = "+jTcodigo.getText());
                   con.getPs().execute();

                   atualizaClientes() ;
                   preenchejTable("");
                   javax.swing.JOptionPane.showMessageDialog(null,"Alterado com sucesso");
                   alterar=1;
                   preenchejTable("");
                   
               }else
               {
                   jTnome.setText(nomeselecionado);
                   JOptionPane.showMessageDialog(null,"Este cliente ja está cadastrado !");
               }
               
           }else if(verificaNome(jTnome.getText())==true)
           {
               JOptionPane.showMessageDialog(null,"Este cliente ja está cadastrado !");
           }else
           {
               con.conecta();
               con.executaSql("insert into cliente(nome_cliente,telefone,cpf,endereco,sexo,cidade_codCidade,qtde_comprado) values(?,?,?,?,?,?,?)");
               con.getPs().setString(1,jTnome.getText());
               con.getPs().setString(2,jTtel.getText());
               con.getPs().setString(3,jTcpf.getText());
               con.getPs().setString(4,jTendereco.getText());
               con.getPs().setString(5,sexo);
               con.getPs().setString(6,jTcidade.getText());
               con.getPs().setString(7,"0");
               con.getPs().execute();

               atualizaClientes() ;
               preenchejTable("");
               javax.swing.JOptionPane.showMessageDialog(null,"Salvo com sucesso");
               //limpaCampos();
               alterar=1;
           }

        } catch (Exception ex) {
             JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

