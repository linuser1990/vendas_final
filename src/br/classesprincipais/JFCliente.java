    public static String formatarString(String texto, String mascara) throws ParseException {  
        MaskFormatter mf = new MaskFormatter(mascara);  
        mf.setValueContainsLiteralCharacters(false);  
        return mf.valueToString(texto);  
    }
    
    private void preenchejTable(String filtro)
    {
         DefaultTableModel modelo2 = (DefaultTableModel) jTable1.getModel();
         modelo2.setRowCount(0);
         
         try
         {
              con.executeStatement("select * from cliente c "
                      + " inner join cidade cid on (c.cidade_CodCidade = cid.codCidade)"
                      + " where nome_cliente like '%"+filtro+"%'order by "+ordem);
              while(con.getRs().next())
              {
                   jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer()
                  {  
                        public Component getTableCellRendererComponent(JTable table, Object value,  
                        boolean isSelected, boolean hasFocus, int row, int column)
                        {  
                            super.getTableCellRendererComponent(table, value, isSelected,  
                                hasFocus, row, column); 
                           
                            if (row%2==0) {  
                                setBackground(Color.LIGHT_GRAY); 
                                
                            }else if (row%2 == 1) {  
                                setBackground(null); 
                            }
                             
                             return this;  
                        }  
                    });  
                  
                  
                  modelo2.addRow(new Object[]{con.getRs().getString("codCliente"),con.getRs().getString("nome_cliente")
                  ,con.getRs().getString("telefone"),con.getRs().getString("cpf"),con.getRs().getString("nome_cidade")
                  ,con.getRs().getString("endereco")});
              }
             
         }catch(Exception e)
         {
             
         }
        
    }
     

