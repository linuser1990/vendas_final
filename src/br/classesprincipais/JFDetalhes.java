    public void ExibeDetalhes(JComboBox combo)
    {
          try
       {
          String autor="",editora="",categoria="";
          jPdetalhes.show();

          String titulo="";
          titulo =(String) combo.getSelectedItem();
          con.conecta();
          con.executeStatement("select * from livro inner join autor on livro.autor_CodAutor = autor.codAutor"+
                  " inner join categoria on livro.categoria_CodCategoria = categoria.codCategoria inner join"+
                  " editora on livro.editora_CodEditora = editora.CodEditora where titulo like '"+titulo+"'");

          while(con.getRs().next())
          {
              jLtitulo.setText(con.getRs().getString("titulo"));
              jLautor.setText(con.getRs().getString("nome_autor"));
              jTresenha.setText(con.getRs().getString("resenha"));
              jLeditora.setText(con.getRs().getString("nome_editora"));
              jLano.setText(con.getRs().getString("data_lanc"));
              jLestoqueatual.setText(con.getRs().getString("estoque_atual"));
              jLprecocusto.setText(con.getRs().getString("preco_custo"));
              jLprecovenda.setText(con.getRs().getString("preco_venda"));
              jLcat.setText(con.getRs().getString("nome_categoria"));
              jLestoqueMinimo.setText(con.getRs().getString("estoque_minimo"));
              jLfoto.setIcon(new ImageIcon("src/br/imagens/"+con.getRs().getString("foto")));


          }

       }catch(Exception e)
       {
           javax.swing.JOptionPane.showMessageDialog(null, e);
       }
    }

