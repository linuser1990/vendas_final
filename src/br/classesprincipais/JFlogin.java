    public void efetuarLogin()
    {
            try
        {
            con.conecta();
              
            con.executaSql("select login,senha from usuario");
            boolean achei=false;
            while(con.getRs().next())
            {
                if(con.getRs().getString("login").equalsIgnoreCase(jTusuario.getText()) && 
                        con.getRs().getString("senha").equalsIgnoreCase(jPsenha.getText()))
                {
                    JFPrincipal aux = new JFPrincipal(jTusuario.getText());
                    aux.setVisible(true);
                    achei=true;
                    //setUsuario(jTusuario.getText());
                    usuario = jTusuario.getText();
                    dispose();
                    break;
                    
                }
                if(cont==3)
                {
                    System.exit(0);
                    break;
                    
                }
            }
            if(achei==false)
            {
                cont++;
                JOptionPane.showMessageDialog(null,"Senha ou usuário incorreto!");
            }
            
        }catch(Exception e)
        {
            System.err.print(e);
        }
    }
